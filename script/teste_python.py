from fractions import gcd

a = int(input())
b = int(input())

print ("SUM:\n" + str(a+b) + "\n")
print ("SUB:\n" + str(a-b) + "\n")
print ("MUL:\n" + str(a*b) + "\n")
print ("DIV:\n" + str(a//b) + "\n")
print ("MOD:\n" + str(a%b) + "\n")
print ("POW:\n" + str(a**25) + "\n")
print ("GCD:\n" + str(gcd(a, b)) + "\n")
